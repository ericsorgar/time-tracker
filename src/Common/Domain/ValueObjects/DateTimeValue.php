<?php

declare(strict_types=1);

namespace TimeTracker\Common\Domain\ValueObjects;

abstract class DateTimeValue
{
    protected $value;

    public function __construct(string $value)
    {
        $this->value = $value;
    }

    public function value(): string
    {
        return $this->value;
    }

    public function __toString()
    {
        return $this->value();
    }
}
